package main

import "fmt"

func main() {
    //String example
    fmt.Println("go" + "lang")
    //Integer and floating point math
    fmt.Println("1+1 = ", 1+1)
    fmt.Println("7.0/3.0 = ", 7.0/3.0)
    //Boolean logic
    fmt.Println(true && false)
    fmt.Println(true || false)
    fmt.Println(!true)
}
